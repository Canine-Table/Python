#!/usr/bin/python3
import math
pi = 3.14
print(round(pi)) # regular rounding
print(math.ceil(pi)) # rounds to the ceiling
print(math.floor(pi)) # rounds to the floor
print(abs(pi)) # how far the integer is from 0
print(math.floor(pow(pi,54))) # power
print(math.sqrt(math.floor(pow(pi,54)))) # square root
array = 4,4,43,23,32,2,3,2,34,23,45,2 # a variable with many values
print(max(array))
print(min(array))